  <ContentPageSubtitle title="À propos de BoRiS" />
  <p>
    Nous avons créé BoRiS dans le cadre du dispositif de startup d’État après
    avoir constaté que le dispositif était encore méconnu et que sa complexité
    empêchait certains ménages éligibles d’y accéder.
  </p>
