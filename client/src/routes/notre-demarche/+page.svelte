  <h2>Pourquoi BoRiS ?</h2>
  <p>
    En nommant ce projet BoRiS, nous cherchons à donner un visage au service que
    nous proposons autour du Bail Réel Solidaire.
  </p>
  <p>
    Vous l'aurez remarqué, BoRiS reprend les lettre du sigle BRS. Mais c'est
    aussi un prénom, celui que nous avons donné à notre chatbot grâce auquel
    vous pouvez déterminer si vous êtes éligible ou non au dispositif. C'est le
    moyen que nous avons trouvé de le rendre plus "humain", comme si vous aviez
    quelqu'un à qui poser toutes vos questions sur ce dispositif qui n'est pas
    toujours simple à comprendre.
  </p>

  <h2>Les grandes étapes du projet</h2>
  <p class="fr-text--lead fr-mb-0"><b>Avril 2023</b></p>
  <p>
    Un agent public identifie un enjeu qui pourrait bénéficier de l'approche
    Startup d'État
  </p>
  <p class="fr-text--lead fr-mb-0">
    <b>Juin-Août 2023 : Lancement de la phase d'investigation</b>
  </p>
  <p>
    L'équipe du projet (1 intrapreneur + 1 coach) ont testé la pertinence du
    projet à travers des interviews d'une vingtaine de personnes (acheteurs en
    BRS, bénéficiaires potentiels, banques, OFS…) et des travaux de recherche.
  </p>
  <p class="fr-text--lead fr-mb-0">
    <b>Août 2023 : Comité d'investissement</b>
  </p>
  <p>
    À partir des conclusions de la phase d'investigation, le comité
    d'investissement a validé la pertinence du projet et l'entrée en phase de
    construction pour 6 à 8 mois.
  </p>
  <p class="fr-text--lead fr-mb-0">
    <b>Septembre 2023 - Mai 2024 : Phase de construction</b>
  </p>
  <p>
    Pendant 6 mois, nous testons l'idée en créant un prototype fonctionnel
    (notamment ce site web et le simulateur d'éligibilité). L'objectif est de
    tester auprès des futurs usagers la pertinence de la solution.
  </p>
  <p>
    Nous avons également associé à notre démarche 6 Organismes Fonciers
    Solidaires des régions Auvergne Rhône-Alpes et PACA. Nous avons à cœur
    d'intégrer tous les acteurs clefs du BRS à chaque étape de le démarche.
  </p>
  <p>
    D'ici la phase de la construction, nous souhaitons améliorer cette
    plateforme avec vos retours et concevoir le dispositif de mise en relation
    entre les ménages qui recherchent un bien en BRS dans une ville spécifique
    et les programmes disponibles dans les quartiers correspondant.
  </p>

  <p class="fr-text--lead fr-mb-0">
    <b>Et après ?</b>
  </p>
  <p>
    Après un nouveau comité d'investissement qui clôturera la phase de
    construction, nous avons la ferme intention de développer cette plateforme
    pour répondre aux enjeux des ménages dans leur recherche d'un logement à
    prix abordable. Nous continuerons à ajouter des informations utiles aux
    acquéreurs à chaque étape de leur parcours.
  </p>
  <p>
    Nous souhaitons également apporter un soutien aux propriétaires en BRS qui
    souhaitent revendre leur logement et se positionner en tiers de confiance.
  </p>
